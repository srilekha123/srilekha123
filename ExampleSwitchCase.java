package lekha1;

public class ExampleSwitchCase {

	public static void main(String[] args) {
		
		int j= 2;
		
		switch(j){
		
		case 0:
			System.out.println("value is 0");
		break;
		case 1:
			System.out.println("value is 1");
		break;
		case 2:
			System.out.println("value is 2");
		break;
		default:
			System.out.println("no value");
			break;
			
		}
	}

		
	}


